public class time {
    private int hour;
    private int minute;
    private double second;

    public time() {
        this.hour = 0;
        this.minute = 0;
        this.second = 0.0;
    }

    public time(int hour, int minute, double second) {
        this.hour = hour;
        this.minute = minute;
        this.second = second;
    }
}
